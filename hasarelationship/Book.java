package com.technoelevate.hasarelationship;

public class Book {

	String author;
	String title;
	public Book(String author, String title) {
		super();
		this.author = author;
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public String getTitle() {
		return title;
	}
	
}
