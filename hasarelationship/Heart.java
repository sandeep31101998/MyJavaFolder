package com.technoelevate.hasarelationship;

public class Heart {

	String heartbeat;
	int weight;
	public Heart(String heartbeat, int weight) {
		super();
		this.heartbeat = heartbeat;
		this.weight = weight;
	}
	public String getHeartbeat() {
		return heartbeat;
	}
	public int getWeight() {
		return weight;
	}
	
	
}
