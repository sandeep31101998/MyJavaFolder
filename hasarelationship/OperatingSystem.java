package com.technoelevate.hasarelationship;

public class OperatingSystem {

	String os ;
	int version;
	public OperatingSystem(String os, int version) {
		super();
		this.os = os;
		this.version = version;
	}
	public String getOs() {
		return os;
	}
	public int getVersion() {
		return version;
	}
	
	
}
